# merges two sorted lists into a new sorted list.

# assume two sorted list are:
list1 = [1,4,6]
list2 = [2,3,5]
# first merge these two list
list = list1+list2
# Then sort the new list
# python provide methods for sorting, website:
#   https://docs.python.org/2/howto/sorting.html
print(sorted(list))
